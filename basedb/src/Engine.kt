package com.suryadigital.leo.basedb

/**
 * List of supported relational database engines.
 */
enum class Engine {
    /**
     * Denotes that the database is a [MySQL](https://www.mysql.com/) database.
     */
    MS_SQL_SERVER,

    /**
     * Denotes that the database is a [PostgreSQL](https://www.postgresql.org/) database.
     */
    POSTGRES,

    /**
     * Denotes that the database is an [Oracle](https://www.oracle.com/in/database/) database.
     */
    ORACLE,
}
